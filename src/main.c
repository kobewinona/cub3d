int	game_exit(int exit_status)
{
	if (exit_status != EXIT_SUCCESS)
		exit(EXIT_FAILURE);
	exit(exit_status);
}

int	read_keys(int key_pressed, void **ptr)
{
	(void)ptr;
	if (key_pressed == ESC)
		game_exit(EXIT_SUCCESS);
	return (SUCCESS);
}

int	render_game(void)
{
	return (SUCCESS);
}

int	main(void)
{
	t_win	*win;

	print_map(testMap);
	if (create_window(SCREEN_WIDTH, SCREEN_HEIGHT, &win) == FAILURE)
		return (EXIT_FAILURE);
	mlx_loop_hook(win->mlx_ptr, render_game, NULL);
	mlx_key_hook(win->win_ptr, read_keys, NULL);
	mlx_hook(win->win_ptr, 17, 0, game_exit, NULL);
	mlx_destroy_window(win->mlx_ptr, win->win_ptr);
	return (EXIT_SUCCESS);
}
